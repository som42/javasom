package Baic_01;

public class Basic_Basic {

	public static void main(String[] args) {
		
		int int_value = 10;
		System.out.printf("\n정수값 출력 : %d   \n", int_value);
		System.out.println("--------------------------");
		
		
		String str = "홍길동";
		System.out.printf("\n문자열 값 출력 : %s", str);
		
		char ch = '홍';
		System.out.printf("\n문자 값 출력 : %c", ch);
		System.out.println("--------------------------");
		
		Double dou = 65.748568;
		System.out.printf("/n소수점 값 출력 : %f", dou);
		System.out.println("--------------------------");
		
		Boolean boo = true;
		System.out.printf("\n참/거짓 값 출력 : %b", boo);
		
		

	}

}
